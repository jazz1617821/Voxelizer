unsigned char getPattern(float value, int x, int y, int z, df_t* df)
{
	unsigned char pattern = '\0';
	if(dataValue(x, y, z, df) < value) pattern |= 1;
	if(dataValue(x + 1, y, z, df) < value) pattern |= 2;
	if(dataValue(x + 1, y + 1, z, df) < value) pattern |= 4;
	if(dataValue(x, y + 1, z, df) < value) pattern |= 8;
	if(dataValue(x, y, z + 1, df) < value) pattern |= 16;
	if(dataValue(x + 1, y, z + 1, df) < value) pattern |= 32;
	if(dataValue(x + 1, y + 1, z + 1, df) < value) pattern |= 64;
	if(dataValue(x, y + 1, z + 1, df) < value) pattern |= 128;
	return pattern;
}

void vertexInterpolation(float value, float* v0, float* v1, float f0, float f1, float* vertex)
{
	int i;

	for (i = 0; i < 3; ++i)
	{
		vertex[i] = v0[i] + (v1[i] - v0[i]) * ((value - f0) / (f1 - f0));
	}
}

